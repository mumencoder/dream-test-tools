        b.dmvar("maptext_width")
        b.dmvar("maptext_height")
        b.dmvar("maptext_x")
        b.dmvar("maptext_y")
        b.dmvar("mouse_over_pointer")
        b.dmvar("mouse_drag_pointer")
        b.dmvar("mouse_drop_pointer")
        b.dmvar("mouse_drop_zone")
        b.dmvar("mouse_opacity")
        b.dmvar("name")
        b.dmvar("opacity")
        b.dmvar("overlays")
