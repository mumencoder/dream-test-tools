
class Common(object):
    def mouse_procs(b):
        b.dmproc("Click")
        b.dmproc("DblClick")
        b.dmproc("MouseDown")
        b.dmproc("MouseDrag")
        b.dmproc("MouseDrop")
        b.dmproc("MouseEntered")
        b.dmproc("MouseExited")
        b.dmproc("MouseMove")
        b.dmproc("MouseUp")
        b.dmproc("MouseWheel")

    def movement_procs(b):
        b.dmproc("Cross")
        b.dmproc("Crossed")
        b.dmproc("Enter")
        b.dmproc("Entered")
        b.dmproc("Exit")
        b.dmproc("Exited")
        b.dmproc("Uncross")
        b.dmproc("Uncrossed")

